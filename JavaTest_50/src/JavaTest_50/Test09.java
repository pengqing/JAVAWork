package JavaTest_50;
/*【程序9】     题目：一个数如果恰好等于它的因子之和，这个数就称为 "完数 "。
 * 例如6=1＋2＋3.编程   找出1000以内的所有完数。*/

public class Test09 {
	public static void main(String[] args){
		//步骤分析：	循环遍历1000，内循环除1000以内的数，余数为0将该数记录，总和+除数。总和与除数相等，打印
		int a=0;
		for(int i=1;i<=1000;i++){
			int sum=0;
			for(int j=1;j<i;j++){
				
				if(i%j==0)
					sum+=j;
				
			}	
			if(sum==i){
				System.out.println(sum);
				a++;
			}
		}
			
			System.out.println("共有完数"+a);
	}
}

	